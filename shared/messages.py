"""Единый стиль и порядок сообщений бота — один источник правды.

КАНОН-ПОРЯДОК элементов (сверху вниз) — решение пользователя:
  1. reply-подсказка  — если сообщение ждёт ответа; ПЕРВОЙ, самой заметной
  2. заголовок        — короткий: эмодзи + <b>, одна строка (НЕ H1)
  3. тело             — контент
  4. доп-подсказки    — примеры и т.п.

Импортируется и ботом, и воркером (``from shared.messages import ...``).
Меняешь стиль reply-подсказки или порядок — меняешь ЗДЕСЬ, меняется ВЕЗДЕ.

Зачем: ревью 328 сообщений показало reply-подсказку в 5+ разных стилях и на
плавающей позиции (то в теле курсивом, то в хвосте после примеров). Из-за
этого её не замечали и слали ответ НОВЫМ сообщением вместо reply. Здесь —
одна reply-подсказка на весь бот.
"""
from __future__ import annotations


def reply_hint_full(action: str | None = None) -> str:
    """Полная reply-подсказка для ask-промптов (ответить нужно ПРЯМО сейчас).

    Явно объясняет жест — иначе юзеры шлют next-message вместо reply и
    проваливаются в catch-all. ``action`` — короткий хвост «…, чтобы <action>».
    """
    tail = f", чтобы {action}" if action else ""
    return (
        f"↩️ <b>Сделай Reply</b> на это сообщение{tail} "
        "(зажми/свайпни сообщение → «Ответить»)"
    )


def reply_hint_compact(action: str) -> str:
    """Компактная reply-подсказка для постоянного контента (списки и т.п.)."""
    return f"↩️ <b>Reply</b> на это сообщение, чтобы {action}"


def compose(*parts: str | None) -> str:
    """Собрать сообщение из блоков, пропуская пустые, через пустую строку.

    Передавай блоки УЖЕ в каноническом порядке:
        compose(reply_hint, heading, body, extra)
    """
    return "\n\n".join(p for p in parts if p)


# Единый набор dedup-команд — ОДИН источник для всех шагов и процессов.
# Раньше расходился: «открой/удали/обнови/сохрани как новую» (alert) vs
# «удали/замени/оставь оба» (open-превью) — юзер видел разные слова на
# разных экранах одного флоу.
DEDUP_COMMANDS = (
    "• <b>открой</b> — покажу старую\n"
    "• <b>удали</b> — удалю новую (старая останется)\n"
    "• <b>обнови</b> — заменю старую новой\n"
    "• <b>сохрани как новую</b> — оставлю обе"
)
